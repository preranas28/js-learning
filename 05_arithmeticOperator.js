

console.log("----------- Addition + operator -----------");
var n1= 10;
var n2= 20;
var result = n1 + n2;
console.log(`Addition is: ${result}`);


console.log("----------- Subtraction - operator -----------");
var n1= 30;
var n2= 20;
var result = n1 - n2;
console.log(`Subtraction is: ${result}`); 


console.log("----------- Multiplication * operator -----------");
var n1= 5;
var n2= 6;
var result = n1 * n2;
console.log(`Multiplication is: ${result}`); 

console.log("----------- Exponential ** operator -----------");
var n1= 5;
var n2= 3;
var result = n1 ** n2;
console.log(`Exponential is: ${result}`); 


console.log("----------- Division / operator -----------");
var n1= 5;
var n2= 3;
var result = n1 / n2;
console.log(`Division is: ${result}`); 


console.log("----------- Modulus % operator -----------");
var n1= 11;
var n2= 3;
var result = n1 % n2;
console.log(`Remainder or Modulus is: ${result}`); 


console.log("----------- Increment ++ operator -----------");
var n1 = 10;
var result = ++n1;   // n1 + 1  prefix increment
var n2 = 20;
var resultValue = n2++; // n2 + 1 postfix increment
console.log(`Result of prefix is : ${result}`);
console.log(`Result of postfix is : ${resultValue}`);


console.log("----------- Decrement -- operator -----------");
var n1 = 10;
var result = --n1;   // n1 - 1  prefix decrement
var n2 = 20;
var resultValue = n2--; // n2 - 1 postfix decrement
console.log(`Result of prefix is : ${result}`);
console.log(`Result of postfix is : ${resultValue}`);
 